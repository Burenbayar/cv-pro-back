export function restoreMongolianWordSpacing(text: string): string {
  let t = text;
  const sorted = [...GLUE_TOKENS].sort((a, b) => b.length - a.length);
  for (const token of sorted) {
    const esc = token.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
    t = t.replace(new RegExp(`([а-яөүёa-z0-9])(?=${esc})`, 'gi'), '$1 ');
    t = t.replace(new RegExp(`(${esc})(?=[а-яөүё])`, 'gi'), '$1 ');
  }
  t = t.replace(/([а-яөүё])(?=(ХХК|Банк|LLC|LLP))/gi, '$1 ');
  t = t.replace(/(он)(?=(АЖЛЫН))/gi, '$1\n');
  return t.replace(/[ \t]{2,}/g, ' ').replace(/\n{3,}/g, '\n\n').trim();
}
